/**
 * Returns the translated title for the given language, or null if lang is "en"
 * or no translation exists.
 */
export function getTitleTranslation(title: string, lang: Lang): string | null {
  if (lang === "en") return null;
  const entry = TITLES[title];
  if (!entry) return null;
  if (lang === "te") return entry.te ?? null;
  if (lang === "ta") return entry.ta ?? null;
  if (lang === "kn") return entry.kn ?? null;
  return entry[lang] ?? null;
}
