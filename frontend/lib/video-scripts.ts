/** Returns the script for a lesson in the requested language, falling back to English. Returns null if no script exists. */
export function getScript(lessonTitle: string, lang: Lang): string | null {
  const entry = LESSON_SCRIPTS[lessonTitle];
  if (!entry) return null;
  if (lang === "hi" && entry.hi) return entry.hi;
  if (lang === "mr" && entry.mr) return entry.mr;
  if (lang === "te" && entry.te) return entry.te;
  if (lang === "ta" && entry.ta) return entry.ta;
  if (lang === "kn" && entry.kn) return entry.kn;
  return entry.en ?? null;
}
